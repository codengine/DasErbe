namespace Game.Shared.Input;

/// <summary>
///     Non-text keys the shared input layer preserves explicitly.
/// </summary>
public enum InputKey
{
    /// <summary>
    ///     No key.
    /// </summary>
    None = 0,

    /// <summary>
    ///     Space.
    /// </summary>
    Space = 2,

    /// <summary>
    ///     Enter.
    /// </summary>
    Enter = 3,

    /// <summary>
    ///     Backspace.
    /// </summary>
    Backspace = 4,

    /// <summary>
    ///     Tab.
    /// </summary>
    Tab = 5
}
